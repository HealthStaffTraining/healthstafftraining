        <div id="why_content">
            <h3>Our History</h3>
            <p>HealthStaff Training Institute (HSTi) began in September 1986 when the education and training division of Pharmacy Enterprises, Inc. formed an education division. When the anticipated expansion of HSTi exceeded the scope of Pharmacy Enterprises, the institute became a privately owned entity, carrying on the name of HealthStaff Training Institute. Since 1986, we have grown to offer a broad range of courses and a well-respected faculty. Our institute continues to evolve to accommodate the needs of the healthcare marketplace and the interest of our students. Every day we carry forward the mission to train and educate motivated and focused individuals into becoming efficient, capable allied health care or business professionals.</p>
        </div>
        <div id="why_content">
            <h3>Our Philosophy</h3>
            <p>HealthStaff Training Institute is dedicated to provide quality, entry-level vocational training in a relatively short period of time at an affordable cost. The industry-focused courses and curriculum are geared to close the employment skills gap between our graduates and the allied health or business job they are trained for. As health care becomes of critical concern, the properly trained health-care professionals becomes a priority. HealthStaff Training Institute endeavors to train and place competent persons. In addition, HealthStaff Training Insitute assumes responsibility for each of our students by using practical teaching techniques, equipment commonly used in the field, industry-focused curriculum, and experienced, qualified faculty to develop the skills and job responsibility of our students. </p>
